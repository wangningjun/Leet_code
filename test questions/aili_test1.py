
def get_total(i, j, n, m,map):
    seat = [[i,j]]
    sum_map = map[i][j]
    while seat:
        i , j = seat[0][0],seat[0][1]
        for a in zip((0, 1, 1, 1, 0, -1, -1, -1), (-1, -1, 0, 1, 1, 1, 0, -1)):
            ai = a[0] + i
            aj = a[1] + j
            if -1<(a[0]+i)<n and -1<(a[1]+j)<m and map[i+a[0]][j+a[1]] !=0:
                seat.append([i+a[0],j+a[1]])
                sum_map +=map[i+a[0]][j+a[1]]
                map[i + a[0]][j + a[1]] = 0
        del seat[0]


    return sum_map



